public class Calculadora {

    public int somar(int x,int y){
        int total = x + y;
        return total;
    }
    public int divi(int x,int y) {
        int total = x / y;
        return total;
    }
    public int sub(int x,int y) {
        int total = x - y;
        return total;
    }
    public int mult(int x,int y) {
        int total = x * y;
        return total;
    }

}

